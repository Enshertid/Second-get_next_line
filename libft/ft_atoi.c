#include "libft.h"

int		ft_atoi(const char *str)
{
	int			i;
	long int	result;
	int			flag;

	i = 0;
	result = 0;
	flag = 1;
	while (ft_isspace(str[i]) == 1)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			flag = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
		if (result < 0 && flag == 1)
			return (-1);
		else if (result < 0 && flag == -1)
			return (0);
	}
	return (result * flag);
}
