#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s2;

	if (s1)
	{
		i = ft_strlen(s1);
		if (!(s2 = (char*)malloc(sizeof(s2) * (i + 1))))
			return (NULL);
		i = 0;
		while (s1[i])
		{
			s2[i] = s1[i];
			i++;
		}
		s2[i] = '\0';
		return (s2);
	}
	return (NULL);
}
