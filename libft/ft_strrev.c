#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = 0;
	while (str[i])
		i++;
	while (j <= i / 2)
	{
		c = str[j];
		str[j] = str[i];
		str[i] = c;
		j++;
	}
	return (str);
}
