#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	size_t				i;
	char				*str;

	if (s)
	{
		str = ft_memalloc(len + 1);
		if (str == NULL)
			return (NULL);
		i = 0;
		while (s[start] && i < len)
		{
			str[i] = s[start];
			i++;
			start++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
